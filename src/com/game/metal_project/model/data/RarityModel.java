package com.game.metal_project.model.data;

public class RarityModel {
	private int id;
	private String rarity_color;
	private String rarity_ch;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getRarity_color() {
		return rarity_color;
	}
	public void setRarity_color(String rarity_color) {
		this.rarity_color = rarity_color;
	}
	public String getRarity_ch() {
		return rarity_ch;
	}
	public void setRarity_ch(String rarity_ch) {
		this.rarity_ch = rarity_ch;
	}
}
